-- ── ROW LEVEL SECURITY ──
alter table public.users enable row level security;
alter table public.query_history enable row level security;
alter table public.query_log enable row level security;
alter table public.subscriptions enable row level security;
alter table public.otp_codes enable row level security;
alter table public.kb_documents enable row level security;
alter table public.settings enable row level security;

-- Users can only read/update their own data
create policy "Users can view own data" on public.users
  for select using (id::text = current_setting('request.jwt.claims', true)::json->>'sub');

create policy "Users can update own data" on public.users
  for update using (id::text = current_setting('request.jwt.claims', true)::json->>'sub');

-- Query history — users see only their own
create policy "Users see own history" on public.query_history
  for all using (user_id::text = current_setting('request.jwt.claims', true)::json->>'sub');

-- KB documents — everyone can read
create policy "Everyone can read KB" on public.kb_documents
  for select using (true);

-- Settings — everyone can read
create policy "Everyone can read settings" on public.settings
  for select using (true);

